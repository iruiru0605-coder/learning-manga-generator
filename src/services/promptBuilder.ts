import type { TeachingUnit } from '@/types'

export function buildMangaPrompt(unit: TeachingUnit, struggle: string) {
  const systemPrompt = `あなたは教育漫画のプロの漫画家です。
与えられた教科書の単元と学習者の苦手情報をもとに、本物の「学習漫画」の台本を8ページ分作成してください。

【最重要：漫画とは何か】
あなたが作るのは「1ページに複数のコマがコマ割りされた漫画」です。
紙芝居（1ページ＝1枚絵＋セリフ）ではありません。
- 1ページは必ず3〜5コマで構成し、コマの大小でリズムをつける
- 見開きではなく、1ページ完結のコマ割り
- 重要なシーンは大ゴマ、説明は中ゴマ、リアクションは小ゴマ
- 右上→左上→右下→左下の順で読まれる日本の漫画のコマ割りを意識する
- 各コマにはセリフ（吹き出し）、背景、キャラクターの表情・動作を指定する

【漫画の基本ルール】
1. 各ページに明確な「引き」（次のページが気になる仕掛け）を入れる
2. 先生役と生徒役のキャラクターが対話形式で学ぶ
3. 難しい概念は具体例や比喩を使ったコマで視覚的に説明する
4. 最後のページ（8ページ目）は「まとめ」として重要ポイントを復習
5. セリフは学習者の学年に適した難易度の日本語を使用する（小学生ならひらがな多め）
6. 各コマには効果音（ジャジャーン、ドキドキ、ピコーン 等）を適宜入れる
7. 学習漫画ならではの「重要ポイント枠」や「公式吹き出し」をコマ内に配置する
8. キャラクターの表情変化（驚き→理解→納得）をコマの連続で表現する

【各ページの構成パターン】
- 1コマ目（大ゴマ）: そのページのテーマを象徴するシーン。驚きや疑問を提示
- 2〜3コマ目（中ゴマ）: 先生が説明。具体例や図解で理解を深める
- 4コマ目（中〜小ゴマ）: 生徒が理解し、次のページへの引き
- 最終ページは感動的なまとめと「がんばったね」のメッセージ

【imageGenerationPromptの書き方】
このフィールドには、画像生成AIが「1ページ全体の漫画」を1枚の画像として生成できるよう、
以下の要素をすべて含めた詳細な英語プロンプトを書いてください：
- ページ全体のコマ割り（panel layout）: 何コマで、どのような配置か
- 各コマの内容: キャラクターの表情・ポーズ・位置、背景、吹き出しの位置とテキスト
- 画風: manga style, black and white, screentone shading
- 重要なポイントは枠で囲む、大きな文字で強調するなどの指示

【出力形式】
以下のJSON形式で厳密に出力してください。余計な説明は一切不要です:
{
  "title": "マンガのタイトル（日本語）",
  "unitName": "${unit.label}",
  "struggleSummary": "苦手内容の要約",
  "targetAudience": "対象学年",
  "pages": [
    {
      "pageNumber": 1,
      "title": "このページのタイトル",
      "summary": "このページで学ぶ内容の簡潔な説明",
      "panels": [
        {
          "panelNumber": 1,
          "size": "大ゴマ",
          "composition": "構図の説明（例：教室の全景ロングショット、先生が黒板を指している）",
          "dialogue": [
            { "speaker": "先生", "text": "セリフ本文", "type": "speech" },
            { "speaker": "生徒", "text": "セリフ本文", "type": "speech" }
          ],
          "narration": "ナレーション文（必要な場合のみ）",
          "effects": "効果音（例：ジャジャーン！）"
        }
      ],
      "imageGenerationPrompt": "英語の画像生成プロンプト。ページ全体のコマ割り・各コマの内容・キャラクターの外見・吹き出し位置・背景・画風を詳細に指定。最低200文字以上。"
    }
  ]
}`

  const userPrompt = `【単元情報】
単元名: ${unit.label}

【学習者の苦手】
${struggle}

上記の単元について、学習者の苦手を克服するための本格的な学習漫画を8ページ作成してください。

【絶対に守ること】
- 各ページは必ず3〜5コマで構成する（1ページ1枚絵の紙芝居は絶対にダメ）
- コマの大小でリズムをつけ、大ゴマ→中ゴマ→小ゴマの流れを意識する
- 各ページの1コマ目は必ず「大ゴマ」で、読者の興味を引きつける
- セリフは吹き出し（speech/thought）とナレーションボックス（narration）を使い分ける
- imageGenerationPromptは必ず「1ページ全体の漫画レイアウト」として英語で書く
- すべてのページが完成した学習漫画として成立していること

【キャラクター設定】
- 先生役: 優しくて博識なキャラクター（名前は「せんせい」）
- 生徒役: 勉強が苦手だけど頑張り屋の子ども（名前は性別に合わせて「たろう」または「はなこ」）`

  return { systemPrompt, userPrompt }
}
